public class XYValue
{
    private float x;
    private float y;

    public XYValue(float x, float y)
    {
        this.x = x;
        this.y = y;

    }//end XYValue

    public float getX()
    {
        return x;
    }

    public void setX(float x)
    {
        this.x = x;
    }

    public float getY()
    {
        return y;
    }

    public void setY(float y)
    {
        this.y = y;
    }
}
